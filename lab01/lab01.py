def multiples_of_3_and_5(n):
    mults = []
    sum = 0
    for num in range(n):
        if num == 0:
            pass
        else:
            if num % 3 == 0:
                mults.append(num)
            elif num % 5 == 0:
                mults.append(num)
    for numbers in mults:
        sum += numbers
    return sum
# (3 points)
